var nota1 = parseFloat(prompt("Por favor informe a nota do primeiro bimestre: "))
var nota2 = parseFloat(prompt("Por favor informe a nota do segundo bimestre: "))
var nota3 = parseFloat(prompt("Por favor informe a nota do terceiro bimestre: "))
var nota4 = parseFloat(prompt("Por favor informe a nota do quarto bimestre: "))

